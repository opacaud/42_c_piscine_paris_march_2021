#include <unistd.h>

int		ft_str_is_alpha(char *str)
{
	int alpha;
	int i;

	alpha = 1;
	i = 0;
	while (str[i])
	{
		if (str[i] < 65 || (str[i] > 90 && str[i] < 97) || str[i] > 122)
			alpha = 0;
		i++;
	}
	return (alpha);
}
